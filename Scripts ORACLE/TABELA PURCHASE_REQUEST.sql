CREATE TABLE PURCHASE_REQUEST(
    CD_PURCHASE_REQUEST NUMBER PRIMARY KEY,
    DS_PURCHASE_JUSTIFICATION VARCHAR2(255),
    DT_REQUIRED_DATE DATE
);

CREATE TABLE ITPURCHASE_REQUEST(
    CD_ITPURCHASE_REQUEST NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CD_PURCHASE_REQUEST NUMBER,
    CD_PRODUCT NUMBER,
    CD_QUANTITY_REQUESTED NUMBER NOT NULL,
    CONSTRAINT FK_PURCHASE_REQUEST
        FOREIGN KEY (CD_PURCHASE_REQUEST)
        REFERENCES PURCHASE_REQUEST (CD_PURCHASE_REQUEST),
    CONSTRAINT FK_PRODUCT
        FOREIGN KEY (CD_PRODUCT)
        REFERENCES PRODUCT (CD_PRODUCT)
);

CREATE SEQUENCE SEQ_PURCHASE_REQUEST
    START WITH 1           
    INCREMENT BY 1         
    NOCACHE                
    NOCYCLE                
;


